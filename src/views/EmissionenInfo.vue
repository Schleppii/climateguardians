  <h2>Was sind Emissionen?</h2>
  <p>
    Emissionen sind Stoffe, die durch natürliche oder menschliche Aktivitäten in
    die Umwelt freigesetzt werden. Diese Substanzen können gasförmig, flüssig,
    fest oder auch Wärmeenergie sein. Je nach Quelle und Art der Emissionen
    beeinflussen sie die Umwelt unterschiedlich stark. Emissionen von
    Schadstoffen in die Luft, wie Kohlendioxid (CO₂) und Methan (CH₄), sind von
    besonderer Bedeutung, da sie erhebliche Auswirkungen auf das Klima und die
    Gesundheit der Menschen haben. Auch im Wasser und Boden können Emissionen
    auftreten, die zur Umweltbelastung beitragen, aber in Bezug auf den
    Klimawandel stehen hauptsächlich Luftemissionen im Fokus.
  </p>
  <p><br /><b>Welche Arten von Emissionen gibt es? </b></p>
  Es gibt verschiedene Arten von Emissionen, die sich hauptsächlich in ihrer
  Zusammensetzung und Quelle unterscheiden. Die wichtigsten Arten sind:
  <p><b>1. Treibhausgasemissionen:</b></p>
  Dazu zählen Kohlendioxid (CO₂), Methan (CH₄), Distickstoffoxid (N₂O) und
  fluorierte Gase. Diese Gase tragen zum Treibhauseffekt bei, indem sie Wärme in
  der Atmosphäre speichern und so zur Erderwärmung beitragen.
  <p><b>2. Luftschadstoffe</b></p>
  Neben Treibhausgasen werden auch andere Schadstoffe wie Stickoxide (NOx),
  Schwefeldioxid (SO₂), Feinstaub und flüchtige organische Verbindungen (VOCs)
  in die Luft emittiert. Diese Schadstoffe belasten die Luftqualität und können
  Atemwegserkrankungen verursachen.
  <p><b>3. Wasser- und Bodenemissionen:</b></p>
  Emissionen in Wasser und Boden umfassen Schadstoffe, die durch industrielle
  Abwässer oder landwirtschaftliche Dünger und Pestizide entstehen und die
  Ökosysteme sowie das Trinkwasser beeinträchtigen können.
  <p><b>4. Lärmemissionen</b></p>
  Auch Lärm zählt zu den Emissionen, die durch Verkehr, Industrie und
  Bauaktivitäten verursacht werden. Lärm hat zwar keine direkten klimatischen
  Auswirkungen, kann jedoch die Lebensqualität und Gesundheit von Menschen
  beeinträchtigen.
  <h2><br />Durch was entstehen Emissionen?</h2>
  Emissionen entstehen durch zahlreiche menschliche Aktivitäten und natürliche
  Prozesse. Die wichtigsten menschlichen Quellen sind:
  <p><b>Verbrennung fossiler Brennstoffe:</b></p>
  Kohle, Erdöl und Erdgas werden in Kraftwerken, Fahrzeugen und Industrieanlagen
  verbrannt, was große Mengen an CO₂ und anderen Schadstoffen freisetzt.
  <p><b>Landwirtschaft und Viehzucht:</b></p>
  Landwirtschaftliche Praktiken, insbesondere die Haltung von Wiederkäuern wie
  Kühen, verursachen hohe Methanemissionen. Auch Düngemittel führen zur
  Freisetzung von Distickstoffoxid.
  <p><b>Abfall und Abwasser:</b></p>
  Die Zersetzung organischer Abfälle produziert Methan und andere schädliche
  Gase, die ebenfalls zur Klimaerwärmung beitragen.
  <p><b>Industrie:</b></p>
  Verschiedene industrielle Prozesse, wie die Zementproduktion, setzen CO₂ und
  andere Schadstoffe frei.

  <h2><br />Wie werden Emissionen gemessen?</h2>
  Emissionen werden auf verschiedene Weisen gemessen, je nach Art des Emittenten
  und der spezifischen Emission:
  <p><b>Direkte Messungen:</b></p>
  An Industrieanlagen oder Fahrzeugen können Sensoren die ausgestoßenen Gase und
  Partikel direkt messen.
  <p><b> Modelle und Berechnungen:</b></p>
  Emissionen, die nicht direkt gemessen werden können, wie etwa die in der
  Landwirtschaft freigesetzten Treibhausgase, werden oft mit Modellrechnungen
  erfasst.
  <p><b>Satellitendaten und Fernerkundung:</b></p>
  Treibhausgase und Luftschadstoffe lassen sich auch aus dem All beobachten und
  erfassen, was vor allem für globale Emissionsverteilungen genutzt wird.
